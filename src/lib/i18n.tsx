// Türkçe tarih/saat/süre metinlerini İngilizce'ye çevirir (mock + serbest metin için)
export function localizeText(str: string | null | undefined, lang: Lang): string {
  if (!str) return ''
  if (lang !== 'en') return str
  let s = str
  for (const [tr, en] of DATE_REPLACEMENTS) s = s.split(tr).join(en)
  return s
}
export function translateBadge(ub: any, lang: Lang): string {
  const key = ub?.badge?.key ?? ub?.key
  const sportName = ub?.sportCategory?.name ?? ub?.sportName
  if (key === 'sport_master_40' && sportName) {
    return lang === 'en' ? `${translateCategory(sportName, lang)} Master` : `${sportName} ustası`
  }
  if (lang === 'en' && key && BADGE_EN[key]) return BADGE_EN[key]
  return ub?.badge?.name ?? ub?.badgeName ?? 'Rozet'
}

interface LangCtx { lang: Lang; setLang: (l: Lang) => void; t: (key: string) => string }
const LanguageContext = createContext<LangCtx>({ lang: 'tr', setLang: () => {}, t: (k) => k })

export function LanguageProvider({ children }: { children: ReactNode }) {
  // Sunucu ve ilk client render'ı 'tr' (hydration uyumu); mount sonrası tercih/ tarayıcı diline göre güncellenir
  const [lang, setLangState] = useState<Lang>('tr')

  useEffect(() => {
    try {
      // Önce kullanıcının daha önce seçtiği dil; yoksa cihaz/tarayıcı diline göre otomatik (EN cihaz → EN, aksi → TR).
      const stored = localStorage.getItem('fitpass_lang') as Lang | null
      if (stored === 'tr' || stored === 'en') { setLangState(stored); return }
      const browser = navigator.language?.toLowerCase().startsWith('en') ? 'en' : 'tr'
      setLangState(browser)
    } catch { /* yoksay */ }
  }, [])

  const setLang = (l: Lang) => {
    setLangState(l)
    try { localStorage.setItem('fitpass_lang', l) } catch { /* yoksay */ }
  }

  const t = (key: string) => dict[lang][key] ?? dict.tr[key] ?? key

  return <LanguageContext.Provider value={{ lang, setLang, t }}>{children}</LanguageContext.Provider>
}

export const useT = () => useContext(LanguageContext)
